package com.sfmuni;

public class Route {
	private String tag;
	private String title;
	public Route()
	{
		
	}
	public String getTag()
	{
		return this.tag;
	}
	public String getTitle()
	{
		return this.title;
	}
	public void setTag(String tag)
	{
		this.tag=tag;
	}
	public void setTitle(String title)
	{
		this.title=title;
	}
	

}
